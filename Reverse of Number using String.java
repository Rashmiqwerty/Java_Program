/* Method-1   :   Reverse of Number using "String" */
import java.util.*;
public class Program1 {
	public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	int n=sc.nextInt();
	String q=Integer.toString(n);
	StringBuffer s=new StringBuffer(q);
	s=s.reverse();
	String st=s.toString();
	int rev=Integer.parseInt(st);
	System.out.print(rev);
		
	}

}

/* Method-2   :   Reverse of Number using Arithmetic Operator and loop */
import java.util.*;
public class Program1 {
	public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	int n=sc.nextInt();
	int rev=0;
	while(n!=0)
	{
		int a=n%10;
		rev=rev*10+a;
		n/=10;
		
	}
	System.out.print(rev);
		
	}

}
